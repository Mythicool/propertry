export const getPropertyById = (id) => {
  return properties.find(property => property.id === parseInt(id));
};

export const getFeaturedProperties = () => {
  return properties.filter(property => property.featured);
};

export const searchProperties = (filters) => {
  return properties.filter(property => {
    // Location filter
    if (filters.location && !property.address.toLowerCase().includes(filters.location.toLowerCase())) {
      return false;
    }

    // Price filters
    if (filters.minPrice && property.price < parseInt(filters.minPrice)) {
      return false;
    }
    if (filters.maxPrice && property.price > parseInt(filters.maxPrice)) {
      return false;
    }

    // Bedroom filter
    if (filters.bedrooms && property.bedrooms < parseInt(filters.bedrooms)) {
      return false;
    }

    // Bathroom filter
    if (filters.bathrooms && property.bathrooms < parseInt(filters.bathrooms)) {
      return false;
    }

    // Property type filter
    if (filters.propertyType && property.propertyType !== filters.propertyType) {
      return false;
    }

    // Square feet filters
    if (filters.minSquareFeet && property.squareFeet < parseInt(filters.minSquareFeet)) {
      return false;
    }
    if (filters.maxSquareFeet && property.squareFeet > parseInt(filters.maxSquareFeet)) {
      return false;
    }

    return true;
  });
};
